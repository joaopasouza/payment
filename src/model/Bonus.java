package model;

/**
 *
 * @author joaopaulo
 */
public class Bonus {

    private final String nome;
    private final double valor;

    public Bonus(String nome, double valor) {
        this.nome = nome;
        this.valor = valor;
    }

    public String getNome() {
        return nome;
    }

    public double getValor() {
        return valor;
    }

}
